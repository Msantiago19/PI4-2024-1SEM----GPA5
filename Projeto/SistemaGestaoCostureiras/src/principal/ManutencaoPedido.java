    public void Executar() {

        if (txtIdPedido.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor, preencha o campo ID");
        } else {
            int id = Integer.parseInt(txtIdPedido.getText());
            PedidoDados a = new PedidoDAO().getPedidoDados(id);

            if (a != null) {
                txtIdProduto.setText(String.valueOf(a.getId_produto()));
                ExecutarNomeProduto();
                txtIdCliente.setText(String.valueOf(a.getId_cliente()));
                ExecutarNomeCliente();
                txtQuantidadeProduto.setText(String.valueOf(a.getQuantidade()));
                txtValorUnidadeProduto.setText(String.valueOf(a.getValor_unitario()));
                txtValorTotal.setText(String.valueOf(a.getValor_total()));
                txtVizualizarPagamento.setText(a.getForma_pagamento());
                txtDataRetirada.setText(a.getData_retirada());
                lblStatus.setText(a.getStatus());

            } else {
                JOptionPane.showMessageDialog(null, "Registro não encontrado.");
            }
        }
    }

    public void ExecutarNomeCliente() {
        int id = Integer.parseInt(txtIdCliente.getText());

        ClienteDados a = new ClienteDAO().getClienteDados(id);
        txtNomeCliente.setText(a.getNome_cliente());
    }

    public void ExecutarNomeProduto() {
        int id = Integer.parseInt(txtIdProduto.getText());

        ProdutoDados a = new produtoDAO().getProdutoDados(id);
        txtNomeProduto.setText(a.getNome_produto());
    }

    public void CalcularValores() {

        try {
            String quantidadeProdutoStr = txtQuantidadeProduto.getText();
            String valorProdutoUnidadeStr = txtValorUnidadeProduto.getText();
            String valorProdutoTotalStr = txtValorTotal.getText();
            String nomeProduto = txtNomeProduto.getText();
            String nomeCliente = txtNomeCliente.getText();
            String formaPagamento = (String) cbxFormaPagamento.getSelectedItem();
            String dataRetirada = txtDataRetirada.getText();

            if (quantidadeProdutoStr.isEmpty() || valorProdutoUnidadeStr.isEmpty() || valorProdutoTotalStr.isEmpty() || formaPagamento.isEmpty() || dataRetirada.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Por favor! Preencha todos os campos");
            } else {
                double valorProduto = 0.0;
                int quantidadeProduto = 0;
                valorProduto = Double.parseDouble(valorProdutoUnidadeStr);
                quantidadeProduto = Integer.parseInt(quantidadeProdutoStr);

                if (valorProduto == 0.0 || quantidadeProduto == 0) {
                    JOptionPane.showMessageDialog(null, "Valor e quantidade do produto não podem ser 0!");
                } else {
                    double valorTotal = valorProduto * quantidadeProduto;

                    lblCliente.setText(nomeCliente);
                    lblProduto.setText(nomeProduto);
                    lblQuantidade.setText(String.valueOf(quantidadeProdutoStr));
                    lblTotal.setText(String.valueOf(valorTotal));
                    txtValorTotal.setText(String.valueOf(valorTotal));
                    txtVizualizarPagamento.setText(formaPagamento);
                    lblPagamento.setText(formaPagamento);
                    lblDataRetirada.setText(dataRetirada);

                }

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Por favor, insira valores numéricos válidos.");
        }
    }

