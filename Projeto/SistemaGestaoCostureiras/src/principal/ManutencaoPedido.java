    public void alterarPedido() {
        String idPedidoStr = txtIdPedido.getText();
        String idClienteStr = txtIdCliente.getText();
        String idProdutoStr = txtIdProduto.getText();

        if (idPedidoStr.isEmpty() || idClienteStr.isEmpty() || idProdutoStr.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor, verifique os campos dos IDs");
        } else {
            if ("Finalizado".equals(lblStatus.getText())) {
                JOptionPane.showMessageDialog(null, "Pedido finalizado, impossível Alterar!");
            } else {
                int id_pedido = Integer.parseInt(idPedidoStr);
                int id_cliente = Integer.parseInt(idClienteStr);
                int id_produto = Integer.parseInt(idProdutoStr);
                int quantidadeProduto = Integer.parseInt(lblQuantidade.getText());
                double valorUnidade = Double.parseDouble(txtValorUnidadeProduto.getText());
                double valorTotal = Double.parseDouble(lblTotal.getText());
                String formaPagamento = lblPagamento.getText();
                String dataRetirada = lblDataRetirada.getText();

                PedidoDados objPedido = new PedidoDados();
                objPedido.setId_pedido(id_pedido);
                objPedido.setId_cliente(id_cliente);
                objPedido.setId_produto(id_produto);
                objPedido.setQuantidade(quantidadeProduto);
                objPedido.setValor_unitario(valorUnidade);
                objPedido.setValor_total(valorTotal);
                objPedido.setForma_pagamento(formaPagamento);
                objPedido.setData_retirada(dataRetirada);
                objPedido.setStatus("Em aberto");

                new PedidoDAO().alterarPedido(objPedido);
                JOptionPane.showMessageDialog(null, "Pedido Alterado com sucesso!");
                LimparDados();
            }
        }
    }

    public void excluirPedido() {
        String idPedidoStr = txtIdPedido.getText();

        if (idPedidoStr.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor, preencha o campo ID");
            return;
        }

        int id_Pedido = Integer.parseInt(idPedidoStr);

        if ("Finalizado".equals(lblStatus.getText())) {
            JOptionPane.showMessageDialog(null, "Pedido finalizado, impossível excluir!");
        } else {
            PedidoDados objPedido = new PedidoDados();
            objPedido.setId_pedido(id_Pedido);

            new PedidoDAO().excluirPedido(objPedido);
            JOptionPane.showMessageDialog(null, "Pedido excluído!");
            LimparDados();
        }
    }


