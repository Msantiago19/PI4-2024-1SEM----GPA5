    public void calcularTotal() {

        try {
            String valorProdutoStr = txtValorProduto.getText();
            String quantidadeProdutoStr = txtQuantidadeProduto.getText();
            String nomeClienteStr = txtNomeCliente.getText();
            String nomeProdutoStr = txtNomeProduto.getText();
            String formaPagamentoStr = (String) cbxFormasPagamento.getSelectedItem();
            String dataRetiradaStr = txtRetirada.getText();

            if (valorProdutoStr.isEmpty() || quantidadeProdutoStr.isEmpty() || nomeClienteStr.isEmpty() || nomeProdutoStr.isEmpty() || formaPagamentoStr.isEmpty() || dataRetiradaStr.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Por favor! Preencha todos os componentes do pedido");
            } else {
                double valorProduto = 0.0;
                int quantidadeProduto = 0;
                valorProduto = Double.parseDouble(valorProdutoStr);
                quantidadeProduto = Integer.parseInt(quantidadeProdutoStr);

                if (valorProduto == 0.0 || quantidadeProduto == 0) {
                    JOptionPane.showMessageDialog(null, "Valor e quantidade do produto não podem ser 0!");
                } else {
                    double valorTotal = valorProduto * quantidadeProduto;

                    lblValorTotal.setText(String.valueOf(valorTotal));
                    lblCliente.setText(nomeClienteStr);
                    lblProduto.setText(nomeProdutoStr);
                    lblQuantidade.setText(quantidadeProdutoStr);
                    lblFormaPagamento.setText(formaPagamentoStr);
                    lblRetirada.setText(dataRetiradaStr);
                }

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Por favor, insira valores numéricos válidos.");

        }
    }

