    public void RealizarPedido(){
        if(lblCliente.getText().isEmpty() || lblProduto.getText().isEmpty() || lblQuantidade.getText().isEmpty() || lblValorTotal.getText().isEmpty() || lblFormaPagamento.getText().isEmpty() || lblRetirada.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Por favor! Calcule os valores antes de confirmar o pedido");
        }else{
            PedidoDados objPedido = new PedidoDados();
            objPedido.setId_cliente(Integer.parseInt(txtIdCliente.getText()));
            objPedido.setId_produto(Integer.parseInt(txtIdProduto.getText()));
            objPedido.setQuantidade(Integer.parseInt(lblQuantidade.getText()));
            objPedido.setValor_unitario(Double.parseDouble(txtValorProduto.getText()));
            objPedido.setValor_total(Double.parseDouble(lblValorTotal.getText()));
            objPedido.setForma_pagamento(lblFormaPagamento.getText());
            objPedido.setData_retirada(txtRetirada.getText());
            objPedido.setStatus("Em aberto");
            
            new PedidoDAO().RealizarPedido(objPedido);
            JOptionPane.showMessageDialog(null, "Pedido Realizado com sucesso!");
            LimparDados();
             
        }
    }


